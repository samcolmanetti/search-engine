package com.samjsoares.soar.model;

public class UrlSeed {

  private long id;

  private String url;

  public UrlSeed () {}

  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }
}
